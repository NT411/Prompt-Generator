# Map key -> token, depending on shell
token_for_key() {
  local k="$1"
  if [[ "$shell_choice" == "1" ]]; then
    # Bash PS1 escapes (stored as literal backslash sequences)
    case "$k" in
      0) printf "%s" '\n' ;;
      1) printf "%s" '\d' ;;
      2) printf "%s" '\t' ;;
      3) printf "%s" '\A' ;;
      4) printf "%s" '\@' ;;
      5) printf "%s" '\u' ;;
      6) printf "%s" '\h' ;;
      7) printf "%s" '\W' ;;
      8) printf "%s" '\w' ;;
    esac
  else
    # Zsh prompt escapes
    case "$k" in
      0) printf "%s" '\n' ;;
      1) printf "%s" '%D' ;;
      2) printf "%s" '%*' ;;
      3) printf "%s" '%T' ;;  # time (HH:MM) in zsh
      4) printf "%s" '%@' ;;  # time with AM/PM in zsh
      5) printf "%s" '%n' ;;
      6) printf "%s" '%m' ;;  # short hostname in zsh
      7) printf "%s" '%1~';;  # last path component in zsh
      8) printf "%s" '%~' ;;
    esac
  fi
}

# Join parts as stored (logical string containing sequences like \n, \d, %D, etc.)
rebuild_logical() {
  local joined=""
  local p
  for p in "${parts[@]}"; do
    joined+="$p"
  done
  printf "%s" "$joined"
}

# Convert the logical prompt into an assignable PS1 line for the chosen shell
emit_ps1_assignment() {
  local logical="$1"

  # Escape single quotes for safe embedding in $'...'
  logical=${logical//\'/\'\\\'\'}

  if [[ "$shell_choice" == "1" ]]; then
    # Bash: we want PS1=$'...' where \n becomes newline, BUT \d etc must survive.
    # So: double-escape backslashes for bash tokens: \d -> \\d, \t -> \\t, etc.
    # Note: do NOT double-escape \n (we want it to remain \n so $'...' makes a newline)
    local s="$logical"
    s=${s//\\/\\\\}       # first, escape all backslashes
    s=${s//\\\\n/\\n}     # restore newline sequences (so they become actual newlines via $'')
    # If user used \a (bell) as literal text, allow it to function too:
    s=${s//\\\\a/\\a}

    printf "PS1=\$'%s'\n" "$s"
  else
    # Zsh: % tokens, and \n should become real newlines => also use $'...'
    printf "PS1=\$'%s'\n" "$logical"
  fi
}

